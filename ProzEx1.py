# Utilizando o laço for em ordem crescente
print("Utilizando o laço 'for' em ordem crescente:")
for numero in range(1, 21):
    if numero != 13:
        print(numero)

# Utilizando o laço for em ordem decrescente
print("\nUtilizando o laço 'for' em ordem decrescente:")
for numero in range(20, 0, -1):
    if numero != 13:
        print(numero)
